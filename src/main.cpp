int main(int /*argc*/, char */*argv*/[])
{
  Camera camera;
  Screen screen;

  while(1) {
    if(kbhit())
    {
      int ch = getch();
      switch(ch)
      {
        case 'd':
          camera.right();
          break;
        case 's':
            camera.donw();
            break;
        case 'a':
            camera.left();
            break;
        case 'w':
            camera.up();
            break;
      }
      
    }

    camera.Show(screen);
    screen.Show();
    screen.Clear();
  }
}
